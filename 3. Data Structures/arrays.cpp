
#include <iostream>
using namespace std;

int main() {
    // Declare and initialize an array of integers
    int arr[5] = {1, 2, 3, 4, 5};

    // Accessing elements using index
    for (int i = 0; i < 5; i++) {
        cout << "Element at index " << i << ": " << arr[i] << endl;
    }

    return 0;
}
